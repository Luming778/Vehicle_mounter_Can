//state ： 成功 HAL_OK ，失败 其他
uint8_t uart3_write(const unsigned char *pData, uint16_t Size, uint32_t Timeout)
{
	uint8_t state;
	while(huart3.gState != HAL_UART_STATE_READY); //??TX
	state = HAL_UART_Transmit(&huart3,pData,Size,Timeout);
	return state;
}


